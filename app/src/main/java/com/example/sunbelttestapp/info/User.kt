package com.example.sunbelttestapp.info

class User {
    private val id = 0
    private val name: String? = null
    private val phone: String? = null
    private val email: String? = null

    fun getId(): Int {
        return id
    }

    fun getName(): String? {
        return name
    }

    fun getPhone(): String? {
        return phone
    }

    fun getEmail(): String? {
        return email
    }
}